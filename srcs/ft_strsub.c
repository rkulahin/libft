#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*ptr;
	size_t	i;

	i = 0;
	ptr = ft_strnew(len);
	if (ptr && s)
		ft_strncpy(ptr, (char *)s + start, len);
	return (ptr);
}
