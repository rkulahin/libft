#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < -9 || n > 9)
		ft_putnbr(n / 10);
	if (n < 0)
	{
		if (n >= -9)
			ft_putchar('-');
		ft_putchar('0' - (n % 10));
	}
	else
		ft_putchar('0' + (n % 10));
}
